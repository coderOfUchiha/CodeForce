#include <iostream>
#include <bits/stdc++.h>
using namespace std;
 int main()
 {
 	int n;
    cin>>n;
 	if (n<=999)
    {
        cout<<"1000";
    }
    if(n>=1000 && n<=1199)
    {
        cout<<"1200";
    }
    if(n>=1200 && n<=1399)
    {
        cout<<"1400";
    }
    if(n>=1400 && n<=1599)
    {
        cout<<"1600";
    }
    if(n>=1600 && n<=1799)
    {
        cout<<"1800";
    }
    if(n>=1800 && n<=1999)
    {
        cout<<"2000";
    }   
    if(n>=2000 && n<=2199)
    {
        cout<<"2200";
    } 
    if(n>=2200 && n<=2399)
    {
        cout<<"2400";
    } 
    if(n>=2400 && n<=2699)
    {
        cout<<"2700";
    } 
    if(n>=2700 && n<=2999)
    {
        cout<<"3000";
    }  
 }